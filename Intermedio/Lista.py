# Declaración de una lista en Python (equivalente a un array en Java)
frutas = ["manzana", "banana", "naranja", "fresa"]

# Acceder a un elemento de la lista
print("Primera fruta:", frutas[0])  # El índice comienza en 0

# Recorrer la lista con un bucle 'for'
for fruta in frutas:
    print("Fruta:", fruta)
